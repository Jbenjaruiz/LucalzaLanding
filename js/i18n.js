// Clase principal de i18n
class I18n {
    constructor() {
        this.currentLang = localStorage.getItem('lucalza_lang') || 'es';
        this.init();
    }
    
    init() {
        this.updateLanguageButton();
        this.translatePage();
        
        // Actualizar lang attribute del HTML
        document.documentElement.lang = this.currentLang;
    }
    
    toggleLanguage() {
        this.currentLang = this.currentLang === 'es' ? 'en' : 'es';
        localStorage.setItem('lucalza_lang', this.currentLang);
        document.documentElement.lang = this.currentLang;
        this.updateLanguageButton();
        this.translatePage();
        
        // Disparar evento personalizado para el banner dinámico
        document.dispatchEvent(new CustomEvent('languageChanged', { 
            detail: { language: this.currentLang } 
        }));
    }
    
    getCurrentLang() {
        return this.currentLang;
    }
    
    updateLanguageButton() {
        const btn = document.getElementById('lang-toggle');
        const btnMobile = document.getElementById('lang-toggle-mobile');
        const newText = this.currentLang === 'es' ? 'EN' : 'ES';
        const newTitle = this.currentLang === 'es' ? 'Switch to English' : 'Cambiar a Español';
        
        if (btn) {
            btn.textContent = newText;
            btn.setAttribute('title', newTitle);
        }
        
        if (btnMobile) {
            btnMobile.textContent = newText;
            btnMobile.setAttribute('title', newTitle);
        }
    }
    
    translatePage() {
        const texts = translations[this.currentLang];
        
        // Traducir elementos con data-i18n
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (texts[key]) {
                // Si es un input o textarea, cambiar placeholder
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    el.placeholder = texts[key];
                } else {
                    // Preservar cualquier HTML interno (como spans, icons)
                    const icon = el.querySelector('span, i');
                    if (icon && el.childNodes.length > 1) {
                        // Si tiene icono al principio, preservarlo
                        if (el.firstElementChild === icon) {
                            el.innerHTML = icon.outerHTML + ' ' + texts[key];
                        } else {
                            el.textContent = texts[key];
                        }
                    } else {
                        el.textContent = texts[key];
                    }
                }
            }
        });
        
        // Traducir placeholders
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (texts[key]) {
                el.placeholder = texts[key];
            }
        });
        
        // Traducir títulos (title)
        document.querySelectorAll('[data-i18n-title]').forEach(el => {
            const key = el.getAttribute('data-i18n-title');
            if (texts[key]) {
                el.title = texts[key];
            }
        });
        
        // Actualizar textos del banner dinámico si existe
        this.updateBannerTexts();
    }
    
    updateBannerTexts() {
        // Los textos del banner se manejan en el script del banner
        // pero actualizamos el título de la página
        document.title = 'LucalzaCorp';
    }
    
    t(key) {
        return translations[this.currentLang][key] || key;
    }
}

// Inicializar cuando el DOM esté listo
document.addEventListener('DOMContentLoaded', function() {
    window.i18n = new I18n();
    
    // Evento para el botón de cambio de idioma (desktop)
    const langBtn = document.getElementById('lang-toggle');
    if (langBtn) {
        langBtn.addEventListener('click', function(e) {
            e.preventDefault();
            window.i18n.toggleLanguage();
        });
    }
    
    // Evento para el botón de cambio de idioma (mobile)
    const langBtnMobile = document.getElementById('lang-toggle-mobile');
    if (langBtnMobile) {
        langBtnMobile.addEventListener('click', function(e) {
            e.preventDefault();
            window.i18n.toggleLanguage();
        });
    }
});

